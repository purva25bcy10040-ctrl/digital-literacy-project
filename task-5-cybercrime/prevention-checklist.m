 Stay Safe Online – Prevention Checklist

- Never share your UPI PIN, OTP, or banking details with anyone.
- Remember: You do NOT need to enter your PIN to receive money.
- Always verify unknown calls claiming to be bank officials.
- Do not click suspicious links sent via SMS or WhatsApp.
- Check payment requests carefully before approving them.
- Enable transaction alerts on your banking apps.
- Use strong passwords and enable two-factor authentication.
- Regularly monitor your bank account for unusual transactions.

Reporting Cybercrime in India
- Report online fraud at: https://cybercrime.gov.in
- Call the Cyber Crime Helpline: 1930 (24x7)
